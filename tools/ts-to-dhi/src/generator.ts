import type { ParsedType, ParsedProperty } from "./parser.js";

const TYPE_MAPPING: Record<string, string> = {
  number: "z.number()",
  string: "z.string()",
  boolean: "z.boolean()",
  bigint: "z.bigint()",
  Date: "z.date()",
  any: "z.any()",
  unknown: "z.unknown()",
  void: "z.void()",
  null: "z.null()",
  undefined: "z.undefined()",
};

